// public/sprites.js — hand-drawn pixel sprite library for cf_ai_werewolf
//
// Each sprite is { palette: string[], grid: string[] }
// where grid is an array of equal-length strings.
//   '.' or ' ' = transparent, '0'..'9','a'..'z' = palette index (parseInt base 36).
//
// renderSprite returns SVG markup with shape-rendering: crispEdges,
// suitable for inserting via innerHTML.

export function renderSprite(spec, opts = {}) {
  const grid = spec.grid;
  const w = grid[0].length;
  const h = grid.length;
  const size = opts.size ?? w * 3;
  const className = opts.className ?? "";
  const style = opts.style ?? "";
  let rects = "";
  for (let y = 0; y < h; y++) {
    const row = grid[y];
    for (let x = 0; x < w; x++) {
      const ch = row[x];
      if (ch === "." || ch === " ") continue;
      const idx = parseInt(ch, 36);
      const fill = spec.palette[idx];
      if (!fill || fill === "transparent") continue;
      rects += `<rect x="${x}" y="${y}" width="1" height="1" fill="${fill}"/>`;
    }
  }
  return `<svg class="sprite ${className}" width="${size}" height="${size}" viewBox="0 0 ${w} ${h}" shape-rendering="crispEdges" style="${style}" xmlns="http://www.w3.org/2000/svg">${rects}</svg>`;
}

